class Example3 
{
	public static void main(String[] args) 
	{
		add();
		sub();
		mul();
		div();
	}
	public static void add()
	{
		int a=20;
		int b=30;
		int res=a+b;
		System.out.println("Addition a and b:"+res);
	}
		public static void sub()
	{
		int a=20;
		int b=30;
		int res=a-b;
		System.out.println("Substraction a and b:"+res);
	}
		public static void mul()
	{
		int a=20;
		int b=30;
		int res=a*b;
		System.out.println("Multiplication a and b:"+res);
	}
		public static void div()
	{
		int a=20;
		int b=30;
		int res=a/b;
		System.out.println("Division a and b:"+res);
	}
}
